import SwiftUI

/*
 iOS15 does not support NavigationStack navigation so this creates a 'RootPresentationMode'
 environment that views can use to create a binding for dismissal of the whole stack of views
 See: https://stackoverflow.com/questions/57334455/how-can-i-pop-to-the-root-view-using-swiftui
 */
struct RootPresentationModeKey: EnvironmentKey {
    static let defaultValue: Binding<RootPresentationMode> = .constant(RootPresentationMode())
}

extension EnvironmentValues {
    var rootPresentationMode: Binding<RootPresentationMode> {
        get { return self[RootPresentationModeKey.self] }
        set { self[RootPresentationModeKey.self] = newValue }
    }
}

typealias RootPresentationMode = Bool

extension RootPresentationMode {
    
    public mutating func dismiss() {
        self.toggle()
    }
}

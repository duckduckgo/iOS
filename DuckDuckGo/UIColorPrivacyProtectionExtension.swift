import UIKit

extension UIColor {

    static var ppGray: UIColor {
        return UIColor(red: 149.0 / 255.0, green: 149.0 / 255.0, blue: 149.0 / 255.0, alpha: 1.0)
    }

    static var ppGreen: UIColor {
        return UIColor(red: 63.0 / 255.0, green: 161.0 / 255.0, blue: 64.0 / 255.0, alpha: 1.0)
    }

    static var ppRed: UIColor {
        return UIColor(red: 189.0 / 255.0, green: 0.0, blue: 23.0 / 255.0, alpha: 1.0)
    }

}
